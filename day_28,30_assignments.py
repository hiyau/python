#day28 assignment
import spacy

# Load the SpaCy English model
nlp = spacy.load("en_core_web_sm")

# Function to perform NER
def named_entity_recognition(text):
    # Process the text using the NLP model
    doc = nlp(text)

    # Extract entities and their types
    print("Named Entities and Their Types:")
    for ent in doc.ents:
        print(f"{ent.text} ({ent.label_})")

# Example usage
text = """
Barack Obama was born on August 4, 1961, in Honolulu, Hawaii.
He served as the 44th president of the United States from 2009 to 2017.
Microsoft Corporation is headquartered in Redmond, Washington.
"""

named_entity_recognition(text)

