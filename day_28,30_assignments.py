from textblob import TextBlob

# Function to perform sentiment analysis
def sentiment_analysis(text):
    # Create a TextBlob object
    blob = TextBlob(text)

    # Get the sentiment polarity
    polarity = blob.sentiment.polarity

    # Determine the sentiment category
    if polarity > 0:
        sentiment = "Positive"
    elif polarity < 0:
        sentiment = "Negative"
    else:
        sentiment = "Neutral"

    # Display the result
    print(f"Text: {text}")
    print(f"Sentiment Polarity: {polarity:.2f}")
    print(f"Overall Sentiment: {sentiment}")

# Example usage
text = "I absolutely love this product! It's fantastic and works perfectly."
sentiment_analysis(text)

text2 = "The service was terrible, and I had an awful experience."
sentiment_analysis(text2)

text3 = "The product is okay, nothing special but not bad either."
sentiment_analysis(text3)